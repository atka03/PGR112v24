package projects.commerce.warehouse.src;

public class Product
{
    //# Fields
    private String name;
    private double price;

    //# Getters
    public String getName() {
        return name;
    }
    public double getPrice() {
        return price;
    }

    //# Setters
    public void setName(String name) {
        this.name = name;
    }
    public void setPrice(double price) {
        this.price = price;
    }
}
